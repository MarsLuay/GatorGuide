const fs = require("fs");
const path = require("path");
const { spawnSync } = require("child_process");

require("ts-node").register({
  skipProject: true,
  transpileOnly: true,
  compilerOptions: {
    module: "CommonJS",
    moduleResolution: "node",
  },
});

const REPO_ROOT = path.resolve(__dirname, "..", "..");
const DISCOVERY_REPORT_PATH = path.resolve(
  REPO_ROOT,
  ".tmp",
  "transfer-planner-primary-source-discovery.json"
);
const OUTPUT_PATH = path.resolve(
  REPO_ROOT,
  "constants",
  "transfer-planner-source",
  "source-manifest-primary-overrides.generated.ts"
);

function hasArg(flag) {
  return process.argv.slice(2).includes(flag);
}

function runDiscovery() {
  const result = spawnSync("node", ["scripts/planner/discover-transfer-planner-primary-sources.cjs"], {
    cwd: REPO_ROOT,
    stdio: "inherit",
    shell: false,
  });

  if (result.status !== 0) {
    throw new Error("Primary-source discovery failed, so promotion could not continue.");
  }
}

function formatHumanDate(isoString) {
  const date = new Date(isoString);
  return new Intl.DateTimeFormat("en-US", {
    month: "long",
    day: "numeric",
    year: "numeric",
    timeZone: "America/Los_Angeles",
  }).format(date);
}

function sanitizeStringLiteral(value) {
  return JSON.stringify(String(value ?? ""));
}

function pickOverrideLabel(owner, suggestedPrimary) {
  return (
    suggestedPrimary.label ||
    suggestedPrimary.anchorText ||
    suggestedPrimary.pageTitle ||
    `${owner.title} primary degree requirements source`
  );
}

function buildOverrideEntries(report) {
  return report.owners
    .filter((owner) => owner?.suggestedPrimary?.confidence === "high")
    .map((owner) => {
      const suggestion = owner.suggestedPrimary;
      const promotedOn = report.generatedAt.slice(0, 10);
      const humanDate = formatHumanDate(report.generatedAt);
      return {
        ownerId: owner.ownerKey,
        label: pickOverrideLabel(owner, suggestion),
        url: suggestion.url,
        note: `Auto-promoted by planner:discover-primary-sources on ${humanDate} from a high-confidence source-discovery match.`,
        confidence: "high",
        score: suggestion.score,
        promotedOn,
        validationNote: `Auto-promoted high-confidence primary source suggestion on ${humanDate}.`,
      };
    })
    .sort((left, right) => left.ownerId.localeCompare(right.ownerId));
}

function loadExistingOverrides() {
  if (!fs.existsSync(OUTPUT_PATH)) {
    return [];
  }

  try {
    delete require.cache[require.resolve(OUTPUT_PATH)];
    const loaded = require(OUTPUT_PATH);
    return loaded.TRANSFER_PLANNER_PROMOTED_PRIMARY_SOURCE_OVERRIDES ?? [];
  } catch (error) {
    console.warn(`Could not load existing promoted primary overrides: ${error.message}`);
    return [];
  }
}

function buildGeneratedFile(entries) {
  const lines = [
    'export type TransferPlannerPromotedPrimarySourceOverride = {',
    '  ownerId: string;',
    '  label: string;',
    '  url: string;',
    '  note?: string;',
    '  confidence: "high";',
    '  score: number;',
    '  promotedOn: string;',
    '  validationNote: string;',
    '};',
    '',
    '// Generated by scripts/planner/promote-transfer-planner-primary-sources.cjs',
    'export const TRANSFER_PLANNER_PROMOTED_PRIMARY_SOURCE_OVERRIDES: TransferPlannerPromotedPrimarySourceOverride[] = [',
  ];

  for (const entry of entries) {
    lines.push("  {");
    lines.push(`    ownerId: ${sanitizeStringLiteral(entry.ownerId)},`);
    lines.push(`    label: ${sanitizeStringLiteral(entry.label)},`);
    lines.push(`    url: ${sanitizeStringLiteral(entry.url)},`);
    lines.push(`    note: ${sanitizeStringLiteral(entry.note)},`);
    lines.push('    confidence: "high",');
    lines.push(`    score: ${entry.score},`);
    lines.push(`    promotedOn: ${sanitizeStringLiteral(entry.promotedOn)},`);
    lines.push(`    validationNote: ${sanitizeStringLiteral(entry.validationNote)},`);
    lines.push("  },");
  }

  lines.push("];");
  lines.push("");

  return lines.join("\n");
}

function main() {
  const discoverFirst = hasArg("--discover-first") || !fs.existsSync(DISCOVERY_REPORT_PATH);

  if (discoverFirst) {
    runDiscovery();
  }

  if (!fs.existsSync(DISCOVERY_REPORT_PATH)) {
    throw new Error(
      `Could not find discovery report at ${DISCOVERY_REPORT_PATH}. Run planner:discover-primary-sources first.`
    );
  }

  const report = JSON.parse(fs.readFileSync(DISCOVERY_REPORT_PATH, "utf8"));
  const existingEntries = loadExistingOverrides();
  const discoveredEntries = buildOverrideEntries(report);
  const entryMap = new Map(existingEntries.map((entry) => [entry.ownerId, entry]));

  for (const entry of discoveredEntries) {
    entryMap.set(entry.ownerId, entry);
  }

  const entries = [...entryMap.values()].sort((left, right) =>
    left.ownerId.localeCompare(right.ownerId)
  );
  fs.writeFileSync(OUTPUT_PATH, buildGeneratedFile(entries));

  console.log(
    `Promoted ${discoveredEntries.length} new high-confidence primary source suggestion(s); ${entries.length} total persisted override(s).`
  );
  console.log(`Override file: ${OUTPUT_PATH}`);
}

try {
  main();
} catch (error) {
  console.error(error);
  process.exit(1);
}
