function renderTemplate() {
  const lines = [
    "# @generated by scripts/dev/generate-env-template.cjs",
    "# GatorGuide client environment template",
    "# Copy this file to .env and fill in local values. Do not commit .env.",
    "#",
    "# Legacy global stub-mode env toggles are intentionally absent.",
    "# Sample and fallback behavior is controlled in code, not this template.",
    "",
  ];

  for (const section of sections) {
    lines.push(`# ${section.title}`);
    for (const note of section.notes ?? []) {
      lines.push(`# ${note}`);
    }
    for (const [key, defaultValue] of section.entries) {
      lines.push(`${key}=${defaultValue}`);
    }
    lines.push("");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

function main() {
  const next = renderTemplate();

  if (isCheck) {
    const current = fs.existsSync(templatePath)
      ? fs.readFileSync(templatePath, "utf8")
      : "";
    if (current !== next) {
      console.error(".env.example is out of sync. Run npm run env:generate.");
      process.exitCode = 1;
    }
    return;
  }

  fs.writeFileSync(templatePath, next, "utf8");
  console.log(`Wrote ${path.relative(rootDir, templatePath)}`);
}

main();
